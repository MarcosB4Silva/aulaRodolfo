// Booleanos - verdadeiro ou falso (true, false)
console.log("02 - Tipo boolean")

// = significa recebe 
console.log(10 > 5);
console.log(10 < 5);

console.log(typeof (10 < 5));

console.log(10 == 10); // igualdade 
console.log(10 == "10"); // igualdade
console.log(10 === "10"); // tipo 

console.log(10 != 3);
console.log(10 != 10);

console.log(5 > 3 && 3 == 2); // verifica igualdade 
console.log(5 > 3 || 3 == 2); // se um lado for verdadeiro

console.log(true && true);  
console.log(false || false);  

console.log(!true);
console.log(!false);